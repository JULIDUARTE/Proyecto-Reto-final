public class Adopciones {

    String Information;
    String Animals;
    String Adopters;
    int Date;

    Adopciones() {}
    Adopciones(String Information, String Animals, String Adopters, int Date)
    {
        this.Information = Information;
        this.Animals = Animals;
        this.Adopters = Adopters;
        this.Date = Date;
    }
}


